public class ForLoopPractice {
    public static void main(String[] args) {

//        First, let's do it with a while loop
        int n = 0;
        while (n <= 10) {
            System.out.println(n);
            n++;

        }
        System.out.println();


//        Same thing, but with a FOR Loop
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
